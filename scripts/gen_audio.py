def post(url: str, body: dict, out_path: str) -> bool:
    req = urllib.request.Request(
        url,
        data=json.dumps(body).encode(),
        headers={"xi-api-key": KEY, "Content-Type": "application/json"},
    )
    try:
        with urllib.request.urlopen(req, timeout=120) as r:
            data = r.read()
        with open(out_path, "wb") as f:
            f.write(data)
        print(f"OK  {os.path.basename(out_path)}  {len(data)//1024}KB")
        return True
    except urllib.error.HTTPError as e:
        print(f"ERR {os.path.basename(out_path)}  HTTP {e.code}: {e.read()[:160]}")
        return False
    except Exception as e:  # noqa: BLE001
        print(f"ERR {os.path.basename(out_path)}  {e}")
        return False


for pax, lines in LINES.items():
    for lid, text in lines.items():
        out = os.path.join(OUT, f"{pax}_{lid}.mp3")
        if os.path.exists(out) and os.path.getsize(out) > 1000:
            print(f"SKIP {pax}_{lid}.mp3 (exists)")
            continue
        # the two cast characters get the clear high bitrate; legacy pax stay small
        fmt = "mp3_44100_64" if pax in ("ganji", "rishta") else "mp3_22050_32"
        ok = post(
            f"https://api.elevenlabs.io/v1/text-to-speech/{VOICES[pax]}?output_format={fmt}",
            {
                "text": text,
                "model_id": "eleven_multilingual_v2",
                "voice_settings": VOICE_SETTINGS.get(pax, DEFAULT_SETTINGS),
            },
            out,
        )
        # extra tempo beyond the API's 1.2 cap — pitch-preserving, per character.
        # ganji 1.0 = API speed only (user: 1.12 on top was too fast)
        ATEMPO = {"ganji": 1.0, "rishta": 1.232}
        if ok and ATEMPO.get(pax, 1.0) != 1.0:
            tmp = out + ".tmp.mp3"
            if os.system(f'ffmpeg -y -v quiet -i "{out}" -filter:a atempo={ATEMPO[pax]} -b:a 64k "{tmp}"') == 0:
                os.replace(tmp, out)
                print(f"     sped up {pax}_{lid}.mp3 (atempo {ATEMPO[pax]})")
        time.sleep(0.4)

